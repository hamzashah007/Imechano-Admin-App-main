import 'package:flutter/material.dart';
import 'package:imechano_admin/styling/text_styles.dart';

class Regular16Black extends Text {
  final String data;
  final TextAlign? textAlign;
  final Locale? locale;
  final bool? softWrap;
  final int? maxLines;

  Regular16Black(
    this.data, {
    this.textAlign,
    this.locale,
    this.softWrap,
    this.maxLines,
  }) : super(
          data,
          textAlign: textAlign,
          locale: locale,
          softWrap: softWrap,
          maxLines: maxLines,
          style: TextStyles.regular16Black,
        );
}

